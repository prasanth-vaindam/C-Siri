// Working of increment and decrement operators
#include <stdio.h>
int main()
{
    int a = 1, b = 10;
    float c = 10.5, d = 100.5;


    printf("++a = %d \n", ++a); // 2
    printf("--b = %d \n", --b); // 9
    printf("++c = %f \n", ++c); // 11.5
    printf("--d = %f \n", --d); // 99.5

   

    return 0;
}